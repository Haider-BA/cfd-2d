void output_uvp(   
                double** U,
		        double ** V,
		        double **P,
		        int il,
		        int ir,
		        int jb,
		        int jt,
		        int omg_i,
		        int omg_j,
		        char* output_file
                ){

    int i,j;

    char szFileName[90];
    FILE *fp = NULL;
    sprintf(szFileName,"%s.%d.%d.vtk",output_file,omg_i,omg_j);

    fp = fopen(szFileName,"w");

    if( fp == NULL){

    char szBuff[80];

    sprintf(szBuff,"Failed to open %s", szFileName);
    ERROR(szBuff);

    return;

    }

    fprintf(fp,"Group 8\n");
    fprintf(fp, "#vtk DataFile \n");
    fprintf(fp, "DATASET STRUCTURED GRID OF SUBDOMAIN\n");
    fprintf(fp, "points starting from   %i  %i \n", il, jb );
    fprintf(fp, "\n");
    fprintf(fp,"\n");
    fprintf(fp,"VECTORS velocity float \n");

    for(j = jb - 1; j <= jt; j++){

        for(i = il - 1; i <= ir; i++){

        fprintf(fp, "%f %f 0\n", (U[i][j] + U[i][j+1])*0.5, (V[i][j] + V[i+1][j]*0.5));

        }

    }

    fprintf(fp,"\n");
    fprintf(fp, "CELL DATA STARTS FROM %i,%i \n", il,jb);
    fprintf(fp, "LOOK UP TABLE default \n");
    fprintf(fp, "SCALAR pressure float");

    for(j = jb ; j <= jt; j++){

        for(i = il; i <= ir; i++){

        fprintf(fp,"%f\n", P[i][j]);

        }
    
    }

}


